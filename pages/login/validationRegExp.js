// 비밀번호는 영문, 숫자 조합으로 8자 이상입력되었는지 체크
const passwordReg = /^(?=.*[a-zA-Z])(?=.*[0-9]).{8,}$/;

// 이메일 형식 체크
const emailReg = new RegExp("([!#-'*+/-9=?A-Z^-~-]+(\.[!#-'*+/-9=?A-Z^-~-]+)*|\"\(\[\]!#-[^-~ \t]|(\\[\t -~]))+\")@([!#-'*+/-9=?A-Z^-~-]+(\.[!#-'*+/-9=?A-Z^-~-]+)*|\[[\t -Z^-~]*])");

export { passwordReg, emailReg };